#!/usr/bin/python3
"""
Defines a class Rectangle
"""


class Rectangle:
    """
    Class to represent a rectangle
    """
    def __init__(self, width=0, height=0):
        """
        Initializes a new Rectangle instance.

        Args:
            width (int): Width of the rectangle.
            height (int): Height of the rectangle.
        """
        self.width = width
        self.height = height
